    private void btguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btguardarActionPerformed
        // TODO add your handling code here:
        UsuarioDAO cm = new UsuarioDAO();
        Usuario usu = new Usuario();
        Usuario val = new Usuario();
        val = c.ValporDNI(jcTipodoc.getSelectedItem().toString(), jtnumdoc.getText());
        if (val != null) {
            JOptionPane.showMessageDialog(null, "El usuario ya existe.");
        } else {
            usu.setId(Integer.parseInt(jtid.getText()));
            usu.setNombre(jtnombre.getText());
            usu.setApellido(jtapellido.getText());
            int rol = jcRol.getSelectedIndex();
            int tipo_doc = jcTipodoc.getSelectedIndex();
            usu.setIdRol(rol);
            usu.setId_tip_doc(tipo_doc);
            usu.setNumeroDocumento(jtnumdoc.getText());
            usu.setSexo(jtsexo.getText());
            usu.setSituacion(jtsituacion.getText());
            if (usu.getId() == 0) {
                cm.adicion(usu);
            } else {
                cm.modifica(usu);
            }
            JOptionPane.showMessageDialog(null, "Se guardaron los datos.");
            dispose();
        }

    }//GEN-LAST:event_btguardarActionPerformed

