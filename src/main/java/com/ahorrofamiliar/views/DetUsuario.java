    public void IniciarFormularios() {
        accionx = BusUsuario.accionx;
        jcTipodoc.removeAllItems();
        jcRol.removeAllItems();
        util.RellenarComboBox("tipo_documento", "Nombre", jcTipodoc);
        util.RellenarComboBox("rol", "Nombre", jcRol);
        jtnombre.setText("");
        jtapellido.setText("");
        jtnumdoc.setText("");
        jtsexo.setText("");
        jtsituacion.setText("");
        if (accionx.equals("0")) {
            jtid.setText("0");
        } else {

            IdRol = BusUsuario.IdRol;
            nombre = BusUsuario.nombre;
            apellido = BusUsuario.apellido;
            TipoIdDoc = BusUsuario.TipoIdDoc;
            NumDoc = BusUsuario.NumDoc;
            sexo = BusUsuario.sexo;
            situacion = BusUsuario.situacion;
            jtid.setText(accionx);

            jtnombre.setText(nombre);
            jtapellido.setText(apellido);
            jtnumdoc.setText(NumDoc);
            jtsexo.setText(sexo);
            jtsituacion.setText(situacion);

            jcRol.setSelectedItem(IdRol);
            jcTipodoc.setSelectedItem(TipoIdDoc);

        }
    }

